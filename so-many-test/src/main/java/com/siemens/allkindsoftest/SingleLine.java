public class SingleLine {

    String id;
    int line;
    Date date;
    String wholeLine;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getLine() {
        return line;
    }

    public void setLine(int line) {
        this.line = line;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getWholeLine() {
        return wholeLine;
    }

    public void setWholeLine(String wholeLine) {
        this.wholeLine = wholeLine;
    }
}
